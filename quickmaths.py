def quickmaths():
    x = int(input("Please provide your number: "))
    y = int(input("Please provide another number: "))
    print(f"""
        {x} + {y} = {x + y}
        {x} - {y} = {x - y}
        {x} * {y} = {x * y}
        {x} / {y} = {x / y}
        {x} // {y} = {x // y}
        {x} % {y} = {x % y}
        {x} ** {y} = {x ** y}
        """)

quickmaths()
